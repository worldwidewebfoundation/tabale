  // chose the string corresponding to the label, depending on the language.
  // if there is a URL with an audio file, use that. Otherwise use the text for the TTS
  public static function say($label, $lang) {
    if (isset(self::$strings[$label][$lang])) {
      if (isset(self::$strings[$label][$lang]['audio'])) {
        return "<audio src='../media/".htmlspecialchars(self::$strings[$label][$lang]['audio'])."'/>";
      } else {
        return self::$strings[$label][$lang]['tts'];
      }
    }
    else
      return $label;
  }
